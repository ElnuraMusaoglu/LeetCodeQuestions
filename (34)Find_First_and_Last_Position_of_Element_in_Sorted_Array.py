'''
Given an array of integers nums sorted in ascending order, find the starting and ending position of a given target value.

If target is not found in the array, return [-1, -1].

You must write an algorithm with O(log n) runtime complexity.



Example 1:

Input: nums = [5,7,7,8,8,10], target = 8
Output: [3,4]
Example 2:

Input: nums = [5,7,7,8,8,10], target = 6
Output: [-1,-1]
Example 3:

Input: nums = [], target = 0
Output: [-1,-1]


Constraints:

0 <= nums.length <= 10**5
-10**9 <= nums[i] <= 10**9
nums is a non-decreasing array.
-10**9 <= target <= 10**9

Runtime: 80 ms, faster than 88.84% of Python3 online submissions for Find First and Last Position of Element in Sorted Array.
Memory Usage: 15.5 MB, less than 51.23% of Python3 online submissions for Find First and Last Position of Element in Sorted Array.

'''
from typing import List


class Solution:
    def searchRange(self, nums: List[int], target: int) -> List[int]:
        def search_helper_recursive(nums, target, left, right):
            if left > right:
                return left
            idx = (left + right) // 2
            if target > nums[idx]:
                left = idx + 1
            else:
                right = idx - 1
            return search_helper_recursive(nums, target, left, right)

        if target is None:
            return [-1, -1]
        if not (0 <= len(nums) < 10**5):
            return [-1, -1]
        if not (-10**9 <= target <= 10**9):
            return [-1, -1]
        lower = search_helper_recursive(nums, target - 0.5, 0, len(nums)-1)
        upper = search_helper_recursive(nums, target + 0.5, 0, len(nums)-1)
        return [-1, -1] if lower == upper else [lower, upper - 1]


